static void readToneData(const uint8_t* data, OPLToneData& toneData, bool is4Op)
{
    uint8_t offset = 0;
    uint8_t num_ops = is4Op ? 4 : 2;
    for (uint8_t op = 0; op < num_ops; op++) {
        memcpy(&toneData.op[op], &data[offset], sizeof(OPLOperatorData));
        offset += sizeof(OPLOperatorData);
    }
    toneData.fbcnt[0] = data[offset++];
    if (is4Op) {
        toneData.fbcnt[1] = data[offset++];
        toneData.flags = OPL_TONE_FLAG_4OP;
    } else {
        toneData.flags = 0;
    }
}

Patch<OPLTone> OPL::getGMPatch(uint8_t bank, uint8_t program)
{
    Patch<OPLTone> patch;

    if (bank == 0) {
        OPLToneData& toneData = patch.params.tone.toneData;
        readToneData(patchData2Op[program], toneData, false);
    } else if (bank == 1) {
        OPLToneData& toneData = patch.params.tone.toneData;
        readToneData(patchData4Op[program], toneData, true);
    }

    return patch;
}

bool OPL::getGMDrumPatch(uint8_t note, Patch<OPLTone>& patch)
{
    if (note >= 35 && note <= 76) {
        patch = Patch<OPLTone>();
        readToneData(drumToneData2Op[note - 35], patch.params.tone.toneData, false);
        patch.params.fixedNoteNum = drumNoteNumbers[note - 35];
        return true;
    } else {
        return false;
    }
}
